import * as vscode from 'vscode';
import { RepositoryCommits, ActivityCommit } from './gitProvider';

interface FlattenedCommit extends ActivityCommit {
  repositoryName: string;
  repositoryPath: string;
}

export class ReportGenerator {
  public static generateMarkdown(repos: RepositoryCommits[], timeframeLabel: string = 'Today'): string {
    const customAuthorName = vscode.workspace.getConfiguration('gitActivityTracker').get<string>('authorName');
    const authorName = customAuthorName || repos[0]?.commits[0]?.authorName || 'Unknown Author';

    let totalCommits = 0;
    let totalMerges = 0;

    for (const repo of repos) {
      totalCommits += repo.commits.length;
      for (const commit of repo.commits) {
        if (commit.parents && commit.parents.length > 1) {
          totalMerges++;
        }
      }
    }

    let markdown = `# Git Activity Report\n\n**Author:** ${authorName}\n**Timeframe:** ${timeframeLabel}\n**Repositories:** ${repos.length}\n**Commits:** ${totalCommits}\n**Merges:** ${totalMerges}\n\n`;

    if (repos.length === 0) {
      markdown += `*No commits found for the selected timeframe.*\n`;
      return markdown;
    }

    const commitsByDate = new Map<string, FlattenedCommit[]>();

    for (const repo of repos) {
      for (const commit of repo.commits) {
        const flatCommit: FlattenedCommit = { ...commit, repositoryName: repo.repositoryName, repositoryPath: repo.repositoryPath };

        const commitDate = commit.authorDate || commit.commitDate || new Date();
        const year = commitDate.getFullYear();
        const month = String(commitDate.getMonth() + 1).padStart(2, '0');
        const day = String(commitDate.getDate()).padStart(2, '0');
        const dateString = `${year}-${month}-${day}`;

        if (!commitsByDate.has(dateString)) {
          commitsByDate.set(dateString, []);
        }
        commitsByDate.get(dateString)!.push(flatCommit);
      }
    }

    if (commitsByDate.size === 0) {
      markdown += `*No commits found for the selected timeframe.*\n`;
      return markdown;
    }

    const sortedDates = Array.from(commitsByDate.keys()).sort().reverse();

    for (const date of sortedDates) {
      markdown += `## Date: ${date}\n\n`;

      const commitsForDate = commitsByDate.get(date)!;
      const reposByDate = new Map<string, FlattenedCommit[]>();

      for (const c of commitsForDate) {
        if (!reposByDate.has(c.repositoryName)) {
          reposByDate.set(c.repositoryName, []);
        }
        reposByDate.get(c.repositoryName)!.push(c);
      }

      const sortedRepos = Array.from(reposByDate.keys()).sort();

      for (const repoName of sortedRepos) {
        const repoCommits = reposByDate.get(repoName)!;
        const repoPath = repoCommits[0].repositoryPath;

        markdown += `### Repository: ${repoName}\n`;
        markdown += `*Path: ${repoPath}*\n\n`;

        repoCommits.sort((a, b) => {
          const dateA = a.authorDate || a.commitDate || new Date(0);
          const dateB = b.authorDate || b.commitDate || new Date(0);
          return dateB.getTime() - dateA.getTime();
        });

        for (const commit of repoCommits) {
          const commitDate = commit.authorDate || commit.commitDate || new Date();
          const hours = String(commitDate.getHours()).padStart(2, '0');
          const minutes = String(commitDate.getMinutes()).padStart(2, '0');
          const seconds = String(commitDate.getSeconds()).padStart(2, '0');
          const timeString = `${hours}:${minutes}:${seconds}`;

          markdown += `- **Branch:** \`${commit.branchName}\`\n`;
          markdown += `- **Timestamp:** ${timeString}\n`;
          markdown += `- **Commit:** \`${commit.hash.substring(0, 7)}\`\n`;
          markdown += `- **Commit message:**\n  > ${commit.message.replace(/\n/g, '\n  > ')}\n\n`;
        }
      }
    }

    const now = new Date();
    const genYear = now.getFullYear();
    const genMonth = String(now.getMonth() + 1).padStart(2, '0');
    const genDay = String(now.getDate()).padStart(2, '0');
    const genHours = String(now.getHours()).padStart(2, '0');
    const genMinutes = String(now.getMinutes()).padStart(2, '0');
    const genSeconds = String(now.getSeconds()).padStart(2, '0');
    const genDateString = `${genYear}-${genMonth}-${genDay}, ${genHours}:${genMinutes}:${genSeconds}`;

    markdown += `---\n\n*Generated by Git Activity Tracker on ${genDateString}*\n`;
    return markdown;
  }
}
